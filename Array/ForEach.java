public class ForEach {
    public static void main(String[] args) {
        int[] arr = {1, 2, 3, 4, 5, 6, 7};
        for(int ele: arr){
            System.out.print(ele+" "); // ye for each loop sirf element ko print krne ke hi kaam mein aata hai aur koi element ko update ni krta hai 
        }
    }
}
